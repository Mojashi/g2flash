#!/usr/bin/env python3
# Generate an 8x8 bitmap font (MSB=leftmost pixel) as a C array for mode_text.c.
# Glyphs are hand-drawn as verifiable ASCII art so there are no hex typos.
# Covers space, 0-9, A-Z, and a few symbols. Index = ASCII - 0x20; gaps -> blank.
G = {}
def g(ch, rows):
    assert len(rows) == 8, ch
    b = []
    for r in rows:
        assert len(r) == 8, (ch, r)
        v = 0
        for c, k in enumerate(r):
            if k == '#': v |= (0x80 >> c)
        b.append(v)
    G[ch] = b

